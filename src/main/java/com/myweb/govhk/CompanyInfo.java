package com.myweb.govhk;

public class CompanyInfo {
    private String no;
    private String name;
    private String address;
    private String infono;
    private String corpname;
    private String cortename;
    private String branchname;
    private String branchename;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getInfono() {
        return infono;
    }

    public void setInfono(String infono) {
        this.infono = infono;
    }

    public String getCorpname() {
        return corpname;
    }

    public void setCorpname(String corpname) {
        this.corpname = corpname;
    }

    public String getCortename() {
        return cortename;
    }

    public void setCortename(String cortename) {
        this.cortename = cortename;
    }

    public String getBranchname() {
        return branchname;
    }

    public void setBranchname(String branchname) {
        this.branchname = branchname;
    }

    public String getBranchename() {
        return branchename;
    }

    public void setBranchename(String branchename) {
        this.branchename = branchename;
    }
}
